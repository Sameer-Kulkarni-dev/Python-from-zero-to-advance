x=int(input("enter the number "))

if(x%7==0):
    print("the number is multiple of 7")
else:   print("the number is not multiple of 7")

# logic behind this is if a number is multiple of 7 then when we divide that number by 7
#  we get the remainder 0, and if the number is not multiple of 7 
# then when we divide that number by 7 we get the remainder other than 0.